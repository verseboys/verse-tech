package com.bolingcavalry.bean;

/**
 * @Description : 用户信息的bean
 * @Author : zq2599@gmail.com
 * @Date : 2018-01-19 12:46
 */
public class UserInfo {
    /**
     * 用户名
     */
    private String name;

    /**
     * 用户年龄
     */
    private int age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
